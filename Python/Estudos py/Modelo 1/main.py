# Inteiro
numero_inteiro = 10
print("Inteiro:", numero_inteiro)

#Real com ponto flutuante
numero_real = 10.5
print("Real:", numero_real)

#type()
print("Tipo de Numero Inteiro:", type(numero_inteiro))
print("Tipo de Numero Real:", type(numero_real))


#soma
soma = 1 + 1 
print("1 + 1 =", soma)

#subtração
subtrcao = 2 - 1
print("2 - 1 =", subtrcao)

#multiplicação
multiplicacao = 2 * 3
print("2 * 3 =", multiplicacao)

# divisão
divisao = 6 / 2
print("5 / 2 =", divisao)
print (int(divisao))
print (float(divisao))

# modulo
modulo = 5 % 2
print("5 % 2 =", modulo)
print(float(modulo))

# devisao inteira
divisao_inteira = 5 // 2
print("5 // 2 =", divisao_inteira)